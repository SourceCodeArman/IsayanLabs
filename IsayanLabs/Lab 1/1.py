#user input
x = input("Enter the first number: ")
y = input("Enter the second number: ")

#storing the numbers as integers 
num1 = int(x)
num2 = int(y)

#finding the sum and product of the 2 numbers 
sum = num1 + num2
product = num1 * num2

#printing the results
print('The sum of ' + x + ' + ' + y + ' is equal to ' + str(sum))
print('The product of ' + x + ' + ' + y + ' is equal to ' + str(product))